package com.class23;

public class Area {
	
	public void getArea (int a, int b) {
		System.out.println("The area of your rectangle is " +(a*b));
	}
	
	public void getArea (int a) {
		System.out.println("The area of your square is " +(a*a));

	}
	
	public void getArea (double a) {
		System.out.println("The area of your cube is " +(a*a*a));
	}

}
