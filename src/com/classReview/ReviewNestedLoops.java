package com.classReview;

public class ReviewNestedLoops {

	public static void main(String[] args) {
		
		//outerloop controls the number of rows
		for (int a=1; a<=5; a++) {
		
			//inner loop is coloumns 10
			for (int b=1; b<=10; b++) {
				
			System.out.print(a*b + " ");
				
			}
			
			System.out.println();
			
		}
	}
}
							
							

	






